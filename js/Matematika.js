export class Matematika {
    /**
     * Sumavimas
     * @param {number} a Pirmas skaitmuo
     * @param {number} b Antras skaimuo
     * @returns {number} Suma
     */
    static suma(a, b) {
        return a + b;
    }

    static skirtumas(a, b) {
        return a - b;
    }

    static dalmuo(a, b) {
        return a / b;
    }

    static sandauga(a, b) {
        return a * b;
    }
}

export function suma(a, b) {
    return a + b;
}

export function skirtumas(a, b) {
    return a - b;
}

export function dalmuo(a, b) {
    return a / b;
}

export function sandauga(a, b) {
    return a * b;
}